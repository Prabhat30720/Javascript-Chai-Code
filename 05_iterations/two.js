// while() loop

let index = 0;

while (index <= 10) {
    console.log(index);
    index = index + 2;
}


const superHeros = ["superman", "batman", "thor", "hulk", "spiderman"];

let arrayIncrement = 0;

while( arrayIncrement < superHeros.length){
    console.log(superHeros[arrayIncrement]);
    arrayIncrement++;
}


// do-while loop

let score = 11;

do {
    console.log(score);
    score++;
} while (score <= 10);